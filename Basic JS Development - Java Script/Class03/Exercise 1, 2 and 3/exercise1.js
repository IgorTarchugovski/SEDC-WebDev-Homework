function sum(x, y) {
    let result = x + y;
    return result;
  }
  
  function substract(x, y) {
    let result = x - y;
    return result;
  }
  
  function multiply(x, y) {
    let result = x * y;
    return result;
  }
  
  function devide(x, y) {
    let result = x / y;
    return result;
  }
  
  console.log(sum(2, 5));
  console.log(substract(4, 2));
  console.log(multiply(2, 2));
  console.log(devide(6, 2));
  